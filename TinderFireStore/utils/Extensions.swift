import UIKit

extension UIColor {
    static func rgb(red: CGFloat, green: CGFloat, blue: CGFloat) -> UIColor {
        return UIColor(red: red/255, green: green/255, blue: blue/255, alpha: 1)
    }
    static func mainBlue() -> UIColor {
        return UIColor.rgb(red: 17, green: 154, blue: 237)
    }
}

struct AnchoredConstraints {
    var top, leading, bottom, trailing, width, height: NSLayoutConstraint?
}


extension UIView {
    
    @discardableResult
    func anchor(top: NSLayoutYAxisAnchor?, leading: NSLayoutXAxisAnchor?, bottom: NSLayoutYAxisAnchor?, trailing: NSLayoutXAxisAnchor?, padding: UIEdgeInsets = .zero, size: CGSize = .zero) -> AnchoredConstraints {
        
        translatesAutoresizingMaskIntoConstraints = false
        var anchoredConstraints = AnchoredConstraints()
        
        if let top = top {
            anchoredConstraints.top = topAnchor.constraint(equalTo: top, constant: padding.top)
        }
        
        if let leading = leading {
            anchoredConstraints.leading = leadingAnchor.constraint(equalTo: leading, constant: padding.left)
        }
        
        if let bottom = bottom {
            anchoredConstraints.bottom = bottomAnchor.constraint(equalTo: bottom, constant: -padding.bottom)
        }
        
        if let trailing = trailing {
            anchoredConstraints.trailing = trailingAnchor.constraint(equalTo: trailing, constant: -padding.right)
        }
        
        if size.width != 0 {
            anchoredConstraints.width = widthAnchor.constraint(equalToConstant: size.width)
        }
        
        if size.height != 0 {
            anchoredConstraints.height = heightAnchor.constraint(equalToConstant: size.height)
        }
        
        [anchoredConstraints.top, anchoredConstraints.leading, anchoredConstraints.bottom, anchoredConstraints.trailing, anchoredConstraints.width, anchoredConstraints.height].forEach{ $0?.isActive = true }
        
        return anchoredConstraints //discards return if nothing catches it
    }
    
    func anchorSize(to view: UIView) {
        widthAnchor.constraint(equalTo: view.widthAnchor).isActive = true
        heightAnchor.constraint(equalTo: view.heightAnchor).isActive = true
    }
    //fill up superview's whole screen
    func fillSuperview(padding: UIEdgeInsets = .zero) {
        translatesAutoresizingMaskIntoConstraints = false
        if let superviewTopAnchor = superview?.topAnchor {
            topAnchor.constraint(equalTo: superviewTopAnchor, constant: padding.top).isActive = true
        }
        
        if let superviewBottomAnchor = superview?.bottomAnchor {
            bottomAnchor.constraint(equalTo: superviewBottomAnchor, constant: -padding.bottom).isActive = true
        }
        
        if let superviewLeadingAnchor = superview?.leadingAnchor {
            leadingAnchor.constraint(equalTo: superviewLeadingAnchor, constant: padding.left).isActive = true
        }
        
        if let superviewTrailingAnchor = superview?.trailingAnchor {
            trailingAnchor.constraint(equalTo: superviewTrailingAnchor, constant: -padding.right).isActive = true
        }
    }
   
    
    @discardableResult
      func fillSuperviewSafeAreaLayoutGuide(padding: UIEdgeInsets = .zero) -> AnchoredConstraints {
          let anchoredConstraints = AnchoredConstraints()
          if #available(iOS 11.0, *) {
              guard let superviewTopAnchor = superview?.safeAreaLayoutGuide.topAnchor,
                  let superviewBottomAnchor = superview?.safeAreaLayoutGuide.bottomAnchor,
                  let superviewLeadingAnchor = superview?.safeAreaLayoutGuide.leadingAnchor,
                  let superviewTrailingAnchor = superview?.safeAreaLayoutGuide.trailingAnchor else {
                      return anchoredConstraints
              }
              return anchor(top: superviewTopAnchor, leading: superviewLeadingAnchor, bottom: superviewBottomAnchor, trailing: superviewTrailingAnchor, padding: padding)
              
          } else {
              return anchoredConstraints
          }
      }
      
      func centerInSuperview(size: CGSize = .zero) {
          translatesAutoresizingMaskIntoConstraints = false
          if let superviewCenterXAnchor = superview?.centerXAnchor {
              centerXAnchor.constraint(equalTo: superviewCenterXAnchor).isActive = true
          }
          
          if let superviewCenterYAnchor = superview?.centerYAnchor {
              centerYAnchor.constraint(equalTo: superviewCenterYAnchor).isActive = true
          }
          
          if size.width != 0 {
              widthAnchor.constraint(equalToConstant: size.width).isActive = true
          }
          
          if size.height != 0 {
              heightAnchor.constraint(equalToConstant: size.height).isActive = true
          }
      }
      
      func centerXTo(_ anchor: NSLayoutXAxisAnchor) {
          translatesAutoresizingMaskIntoConstraints = false
          centerXAnchor.constraint(equalTo: anchor).isActive = true
      }
      
      func centerYTo(_ anchor: NSLayoutYAxisAnchor) {
          translatesAutoresizingMaskIntoConstraints = false
          centerYAnchor.constraint(equalTo: anchor).isActive = true
      }
      
      func centerXToSuperview() {
          translatesAutoresizingMaskIntoConstraints = false
          if let superviewCenterXAnchor = superview?.centerXAnchor {
              centerXAnchor.constraint(equalTo: superviewCenterXAnchor).isActive = true
          }
      }
      
     func centerYToSuperview() {
          translatesAutoresizingMaskIntoConstraints = false
          if let superviewCenterYAnchor = superview?.centerYAnchor {
              centerYAnchor.constraint(equalTo: superviewCenterYAnchor).isActive = true
          }
      }
      
      @discardableResult
      func constrainHeight(_ constant: CGFloat) -> AnchoredConstraints {
          translatesAutoresizingMaskIntoConstraints = false
          var anchoredConstraints = AnchoredConstraints()
          anchoredConstraints.height = heightAnchor.constraint(equalToConstant: constant)
          anchoredConstraints.height?.isActive = true
          return anchoredConstraints
      }
      
      @discardableResult
      func constrainWidth(_ constant: CGFloat) -> AnchoredConstraints {
          translatesAutoresizingMaskIntoConstraints = false
          var anchoredConstraints = AnchoredConstraints()
          anchoredConstraints.width = widthAnchor.constraint(equalToConstant: constant)
          anchoredConstraints.width?.isActive = true
          return anchoredConstraints
      }
    func setupShadow(opacity: Float = 0, radius: CGFloat = 0, offset: CGSize = .zero, color: UIColor = .black) {
        layer.shadowOpacity = opacity
        layer.shadowRadius = radius
        layer.shadowOffset = offset
        layer.shadowColor = color.cgColor
    }
    
    convenience public init(backgroundColor: UIColor = .clear, opacity: Float = 1) {
        self.init(frame: .zero)
        self.backgroundColor = backgroundColor
        self.layer.opacity = opacity
    }
    
    
    fileprivate func _stack(_ axis: NSLayoutConstraint.Axis = .vertical, views: [UIView], spacing: CGFloat = 0, alignment: UIStackView.Alignment = .fill, distribution: UIStackView.Distribution = .fill) -> UIStackView {
        let stackView = UIStackView(arrangedSubviews: views)
        stackView.axis = axis
        stackView.spacing = spacing
        stackView.alignment = alignment
        stackView.distribution = distribution
        addSubview(stackView)
        stackView.fillSuperview()
        return stackView
    }
    
    @discardableResult
    open func stack(_ views: UIView..., spacing: CGFloat = 0, alignment: UIStackView.Alignment = .fill, distribution: UIStackView.Distribution = .fill) -> UIStackView {
        return _stack(.vertical, views: views, spacing: spacing, alignment: alignment, distribution: distribution)
    }
    
    @discardableResult
    open func hstack(_ views: UIView..., spacing: CGFloat = 0, alignment: UIStackView.Alignment = .fill, distribution: UIStackView.Distribution = .fill) -> UIStackView {
        return _stack(.horizontal, views: views, spacing: spacing, alignment: alignment, distribution: distribution)
    }
    
    @discardableResult
    open func withSize<T: UIView>(_ size: CGSize) -> T {
        translatesAutoresizingMaskIntoConstraints = false
        widthAnchor.constraint(equalToConstant: size.width).isActive = true
        heightAnchor.constraint(equalToConstant: size.height).isActive = true
        return self as! T
    }
    
    @discardableResult
    open func withHeight(_ height: CGFloat) -> UIView {
        translatesAutoresizingMaskIntoConstraints = false
        heightAnchor.constraint(equalToConstant: height).isActive = true
        return self
    }
    
    @discardableResult
    open func withWidth(_ width: CGFloat) -> UIView {
        translatesAutoresizingMaskIntoConstraints = false
        widthAnchor.constraint(equalToConstant: width).isActive = true
        return self
    }
    
    @discardableResult
    func withBorder<T: UIView>(width: CGFloat, color: UIColor) -> T {
        layer.borderWidth = width
        layer.borderColor = color.cgColor
        return self as! T
    }
    
}

extension UIViewController {
    func setupHideKeyboardWhenTappedAround() {
        let tap: UITapGestureRecognizer = UITapGestureRecognizer(target: self, action: #selector(UIViewController.dismissKeyboard))
        tap.cancelsTouchesInView = false
        view.addGestureRecognizer(tap)
    }
    @objc func dismissKeyboard() {
        view.endEditing(true)
    }
}

